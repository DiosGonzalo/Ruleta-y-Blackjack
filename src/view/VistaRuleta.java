	public static void mostrarRuleta(String[] ruleta, int posicionActual) { // Todo lo que tiene nombre es hecho por
																			// Gonzalo Dios.

		// PARA MODIFICAR EL TAMAÑO, SI SUBES 1 TIENES QUE BAJAR EL OTRO TANTO COMO
		// HAYAS SUBIDO AL ANTERIOR:
		// Si subes 1 de alto (de 3 a 4), tienes que bajar uno de ancho, (15 a 14)
		int ancho = 15; // PABLO GARCÍA MARÍA
		int alto = 3; // PABLO GARCÍA MARÍA

		// POR CADA TABULACION SON 8 ESPACIOS.
		String cadena = ""; // PABLO GARCÍA MARÍA
		int númeroTabs = 9; // PABLO GARCÍA MARÍA

		String cadena2 = ""; // PABLO GARCÍA MARÍA
		int númeroEspacion = 3; // PABLO GARCÍA MARÍA

		int inicioFilaAlta, inicioFilaBaja, finalFilaAlta, finalFilaBaja, inicioFilaIzq, inicioFilaDer, finalFilaIzq,
				finalFilaDer; // PABLO GARCÍA MARÍA

		inicioFilaAlta = 0; // PABLO GARCÍA MARÍA
		finalFilaAlta = ancho; // PABLO GARCÍA MARÍA

		inicioFilaIzq = 37; // PABLO GARCÍA MARÍA
		finalFilaIzq = inicioFilaIzq - alto; // PABLO GARCÍA MARÍA

		inicioFilaDer = finalFilaAlta + 1; // PABLO GARCÍA MARÍA
		finalFilaDer = inicioFilaDer + alto; // PABLO GARCÍA MARÍA

		inicioFilaBaja = finalFilaIzq; // PABLO GARCÍA MARÍA
		finalFilaBaja = inicioFilaBaja - ancho; // PABLO GARCÍA MARÍA

		view.tools.Herramientas.limpiarPantalla();

		System.out.print("\r");
		System.out.println("Ruleta:");

		System.out.print("|");
		for (int i = inicioFilaAlta; i <= finalFilaAlta; i++) { // PABLO GARCÍA MARÍA
			if (i == posicionActual) {// Gonzalo Dios
				System.out.print(" ⚽ |");
			} else {
				if (i < 10) {// Gonzalo Dios
					System.out.print(" " + ruleta[i] + "  |");
				} else {
					System.out.print(" " + ruleta[i] + " |");
				}
			}
		}
		System.out.println();

		for (int index = 0; index < númeroTabs; index++) {
			cadena += "\t";
		}

		for (int index = 0; index < númeroEspacion; index++) {
			cadena2 += " ";
		}

		for (int i = inicioFilaDer, j = inicioFilaIzq; i < finalFilaDer && j > finalFilaIzq; i++, j--) { // PABLO GARCÍA
																											// MARÍA
			if (i == posicionActual) {
				System.out.printf("| %2s |%s%s| ⚽ |\n", ruleta[j], cadena, cadena2);
			} else if (j == posicionActual) {
				System.out.printf("| ⚽ |%s%s| %2s |\n", cadena, cadena2, ruleta[i]);
			} else {
				System.out.printf("| %2s |%s%s| %2s |\n", ruleta[j], cadena, cadena2, ruleta[i]);

			}
		}
		System.out.print("| ");
		for (int i = inicioFilaBaja; i > finalFilaBaja - 1; i--) { // PABLO GARCÍA MARÍA
			if (i == posicionActual) {
				System.out.print("⚽ | ");
			} else {
				System.out.print(ruleta[i] + " | ");
			}
		}
		System.out.println();

	}

